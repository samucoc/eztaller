    {
        foreach ($this->roles as $index => $role) {

            $rol = Role::where('name', '=', "{$role['name']}")->get();

            if (count($rol->toArray()) == 0) {
                $data = [
                    'ENC_NIV_CENTRAL' => [
                        'type' => RoleModel::NACIONAL,
                        'hierarchy' => 1
                    ],
                    'EQ_AS_TEC_NIV_CENTRAL' => [
                        'type' => RoleModel::NACIONAL,
                        'hierarchy' => 1
                    ],
                    'ENC_REG_SEREMI' => [
                        'type' => RoleModel::REGIONAL,
                        'hierarchy' => 2
                    ],
                    'PROF_APO_PSICOSOCIAL' => [
                        'type' => RoleModel::REGIONAL,
                        'hierarchy' => 2
                    ],
                    'ASSIST_TEC_FOSIS' => [
                        'type' => RoleModel::REGIONAL,
                        'hierarchy' => 2
                    ],
                    'COORD_EJECUTOR' => [
                        'type' => RoleModel::COMUNAL,
                        'hierarchy' => 3
                    ],
                    'PROF_CONSTRUCTIVO' => [
                        'type' => RoleModel::COMUNAL,
                        'hierarchy' => 3
                    ],
                    'PROF_SOCIAL' => [
                        'type' => RoleModel::COMUNAL,
                        'hierarchy' => 3
                    ],
                ];
                if (array_key_exists($role['name'], $data)) {
                    $type = $data[$role['name']]['type'];
                    $hierarchy = $data[$role['name']]['hierarchy'];
                } else {
                    $type = '';
                    $hierarchy = 0;
                }
                $obj = new Role([
                    'name' => $role['name'],
                    'description' => $role['description'],
                    'type' => $type,
                    'hierarchy' => $hierarchy,
                ]);
                $obj->save();
            }
            $user = User::where('rut', $role['user']['rut'])->first();
            if (!$user) {
                $comunas = $role['comunas'];
                $role['user']['clave'] = 'abc';
                $role['user']['password'] = Hash::make($role['user']['password']);
                $user = new User($role['user']);

                $rol = Role::where('name', '=', "{$role['name']}")->get();

                $user->save();
                $user->assignRole($rol);
                //$user->comunas()->sync($comunas);
            }
        }
        foreach ($this->permissions as $permission) {
            $obj = new Permission([
                'name' => $permission['name'],
                'description' => $permission['description']
            ]);
            $obj->save();
            $roles = $permission['roles'];
            if (in_array('*', $roles)) {
                $roles = array_map(function ($role) {
                    return $role['name'];
                }, $this->roles);
            }
            $obj->syncRoles($roles);
        }
    }
